    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        if (jTtipoentrada.getSelectedRow() != -1) {

            TipoEntrada te = new TipoEntrada();
            TipoEntradaDAO dao = new TipoEntradaDAO();

            te.setId((int) jTtipoentrada.getValueAt(jTtipoentrada.getSelectedRow(), 0));
            
            dao.delete(te);

            readJTable();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha para remover.");
        }

    }//GEN-LAST:event_jButton5ActionPerformed


