    public void readJTable() {
        
        DefaultTableModel modelo = (DefaultTableModel) jTtipoentrada.getModel();
        modelo.setNumRows(0);
        TipoEntradaDAO tedao = new TipoEntradaDAO();

        for (TipoEntrada te : tedao.read()) {

            modelo.addRow(new Object[]{
                te.getId(),
                te.getDes_entrada()
            });

        }
   
    }

